export class I18nService {
    private static currentLocale: Locale = 'zh';

    public static getLocale(): Locale {
        return this.currentLocale;
    }

    public static setLocale(locale: Locale): void {
        this.currentLocale = locale;
        const info = LOCALES.find(l => l.code === locale);
        document.documentElement.lang = locale;
        document.documentElement.dir = info?.dir || 'ltr';
        localStorage.setItem('mecha-locale', locale);
    }

    public static loadSavedLocale(): void {
        const saved = localStorage.getItem('mecha-locale') as Locale | null;
        if (saved && translations[saved]) {
            this.setLocale(saved);
        } else {
            this.setLocale(this.detectSystemLanguage());
        }
    }

    private static detectSystemLanguage(): Locale {
        const langs = navigator.languages || [navigator.language || (navigator as any).userLanguage || ''];
        for (const lang of langs) {
            const code = lang.split('-')[0].toLowerCase() as Locale;
            if (translations[code]) return code;
        }
        for (const lang of langs) {
            const full = lang.toLowerCase();
            for (const key of Object.keys(translations)) {
                if (full.startsWith(key)) return key as Locale;
            }
        }
        return 'en';
    }

    public static t(key: string, params?: Record<string, string>): string {
        const locale = this.currentLocale;
        let text = translations[locale]?.[key] || translations['en']?.[key] || key;
        if (params) {
            for (const [k, v] of Object.entries(params)) {
                text = text.replace(`{${k}}`, v);
            }
        }
        return text;
    }

    public static getAvailableLocales(): LocaleInfo[] {
        return LOCALES;
    }
}
